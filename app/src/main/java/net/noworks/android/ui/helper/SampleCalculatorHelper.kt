package net.noworks.android.ui.helper

/**
 * ユニットテスト用のサンプル実装
 *
 */
class SampleCalculatorHelper {
    /**
     * 加算した結果を返します。
     *
     * @param x
     * @param y
     * @return 加算した結果
     */
    fun sum(x: Int, y: Int): Int = x + y

    /**
     * 減算した結果を返します。
     *
     * @param x
     * @param y
     * @return 減算した結果
     */
    fun subtract(x: Int, y: Int): Int = x - y
}